#include <stdio.h>
#include <unistd.h>

int main (void)
{
	char c = 'A';
	char *str = "Bonjour";
	int	d = 123;
	printf("%c>>>%s>>>%d\n", c, str, d);
	printf("%c>>>%*s>>>%d\n", 48, c, str, d);
	printf("%2$*3$c>>>%4$s>>>%1$d\n", d, 48, c, str);
	return (0);
}
